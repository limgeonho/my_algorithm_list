import math
# math 라이브러리 관련

# ============================================================
# 1. 팩토리얼 => math.factorial(x) => 엄청빠름...

print(math.factorial(50))
# 30414093201713378043612608166064768844377641568960512000000000000

# ============================================================
# 2. 최대공약수 => math.gcd(a, b)

print(math.gcd(21, 14))
# 7

# ============================================================
# 3. 최소공배수 => math.gcd(a, b)를 이용!
# 최소공배수 = (수1 * 수2) // (수1과 수2의 최대공약수)

a = 21
b = 14
print((a * b) // math.gcd(a, b))

# ============================================================
